void rotary_encoder(void *pvParameters)
{
    gpio_set_direction(ENCODER_CLK, GPIO_MODE_INPUT);
    gpio_set_pull_mode(ENCODER_CLK, GPIO_PULLUP_ONLY);

    gpio_set_direction(ENCODER_DT, GPIO_MODE_INPUT);
    gpio_set_pull_mode(ENCODER_DT, GPIO_PULLUP_ONLY);

    gpio_set_direction(ENCODER_SW, GPIO_MODE_INPUT);
    gpio_set_pull_mode(ENCODER_SW, GPIO_PULLUP_ONLY);

    while (1) {
        int clk_state = gpio_get_level(ENCODER_CLK);
        int dt_state = gpio_get_level(ENCODER_DT);

        if (clk_state != last_clk_state && clk_state == 0) {
            if (dt_state != clk_state) {
                counter++; // Clockwise
            } else {
                counter--; // Counter-clockwise
            }
            ESP_LOGI(TAG, "Counter: %d", counter);
        }

        last_clk_state = clk_state;

        if (gpio_get_level(ENCODER_SW) == 0) {
            ESP_LOGI(TAG, "Switch pressed!");
            flag1 = true;
            vTaskDelay(pdMS_TO_TICKS(200));
        }

        vTaskDelay(pdMS_TO_TICKS(10));
    }
}

// Menu Drawing Function
void draw_menu(u8g2_t *u8g2, int selected)
{
    u8g2_ClearBuffer(u8g2);
    u8g2_SetFont(u8g2, u8g2_font_cu12_tr);  // Ensure font is set before drawing
    for (int i = 0; i < itemCount; i++) {
        int y = 12 + i * 12;
        if (i == selected) {
            u8g2_DrawBox(u8g2, 0, y - 10, 128, 12);
            u8g2_SetDrawColor(u8g2, 0);
            u8g2_DrawStr(u8g2, 5, y, menuItems[i]);
            u8g2_SetDrawColor(u8g2, 1);
        } else {
            u8g2_DrawStr(u8g2, 5, y, menuItems[i]);
        }
    }
    u8g2_SendBuffer(u8g2);
}

